    public void insert(String name, String sn, String course, String sy, String lnk) {
        String sql = "INSERT INTO record(name,sn,course,sy,pp) VALUES(?,?,?,?,?)";
 
        try (Connection conn = this.connect();
                PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, name);
            pstmt.setString(2, sn);
            pstmt.setString(3, course);
            pstmt.setString(4, sy);
            pstmt.setString(5, lnk);
            pstmt.executeUpdate();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
    public boolean selectAll(String sn_key){
        String sql = "SELECT * FROM record WHERE sn =" +sn_key;
        
        try (Connection conn = this.connect();
             Statement stmt  = conn.createStatement();
             ResultSet rs    = stmt.executeQuery(sql)){
           
            // loop through the result set
            //System.out.println(rs.next());
            if(rs.next()) {
                pname = rs.getString("name");
                psn = rs.getString("sn");
                pcourse = rs.getString("course");
                psy = rs.getString("sy");
                ppp = rs.getString("pp");
                requested = rs.getString("requested");
                id = Integer.parseInt(rs.getString("id"));
                
                if("true".equals(requested)){
                    System.out.println("Already Requested");
                    return false;
                }
//                cpnumber = rs.getString("cpnumber");
                
                return true;

            }
            else{
                return false;
            }
            
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        return false;
    }
    
    
